    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        SupervisionCore core = new SupervisionCore();
        String selected = null;
        String key;
        String notes;
        for (Enumeration<AbstractButton> buttons = staffList.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                selected =  button.getText();
            }
        }
        key = "supervisionTracker" + selected + "Notes";

        notes = outputPane.getText();
        core.setSupervisionNotes(key, notes);
    }//GEN-LAST:event_saveButtonActionPerformed

